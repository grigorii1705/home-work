#Напишите try...except...else, где else выводит "Ошибок нет".
def safe_division(a, b):
    print(f"--- Попытка деления {a} на {b} ---")
    try:
        # Потенциально опасная операция
        result = a / b
    except ZeroDivisionError:
        # Этот блок выполняется ТОЛЬКО если произошло деление на ноль
        print("Ошибка (except): Произошло деление на ноль!")
    else:
        # Этот блок выполняется ТОЛЬКО если код в 'try' выполнился БЕЗ ОШИБОК
        print(f"Результат (else): Деление успешно завершено. Результат = {result}")
        return result
    finally:
        # Этот блок выполняется всегда (опционально)
        print("--- Завершение операции деления ---\n")
# Пример 1: Ошибки нет, сработает блок 'else'
safe_division(10, 2)

# Пример 2: Есть ошибка, сработает блок 'except'
safe_division(10, 0)