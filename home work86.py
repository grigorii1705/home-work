#Создайте вложенные блоки try...except, где внутренняя ошибка пробрасывается наружу.
def inner_file_checker(filename):
    # Внутренний блок try: отвечает за низкоуровневую работу с файлом
    try:
        print(f"[Внутри функции] Пытаюсь открыть '{filename}'...")
        f = open(filename, 'r')
        content = f.read()
        f.close()
        print("[Внутри функции] Файл успешно прочитан.")
        return content
    except FileNotFoundError:
        # Ловим ошибку здесь, печатаем техническое сообщение
        print("[Внутри функции] Ошибка! Не могу найти файл. Пробрасываю ошибку наверх.")
        # Команда raise без параметров пробрасывает исходную ошибку наружу
        raise
print("[Снаружи] Запуск программы.")
try:
    # Вызываем функцию, которая может пробросить ошибку
    inner_file_checker("non_existent_file.txt")
except FileNotFoundError:
    # Ловим проброшенную ошибку здесь и показываем сообщение пользователю
    print("[Снаружи] Я поймал проброшенную ошибку. Сообщаю пользователю: ФАЙЛ НЕ НАЙДЕН!")

print("[Снаружи] Программа завершена.")